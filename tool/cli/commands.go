type (
	// CreateShortURLShortURLCommand is the command line data structure for the create_short_url action of short_url
	CreateShortURLShortURLCommand struct {
		Payload     string
		ContentType string
		PrettyPrint bool
	}

	// GetShortURLShortURLCommand is the command line data structure for the get_short_url action of short_url
	GetShortURLShortURLCommand struct {
		ShortURLHash string
		PrettyPrint  bool
	}

	// RedirectShortURLShortURLCommand is the command line data structure for the redirect_short_url action of short_url
	RedirectShortURLShortURLCommand struct {
		ShortURLHash string
		PrettyPrint  bool
	}
)

// RegisterCommands registers the resource action CLI commands.
func RegisterCommands(app *cobra.Command, c *client.Client) {
	var command, sub *cobra.Command
	command = &cobra.Command{
		Use:   "createShortURL",
		Short: `Create a short url`,
	}
	tmp1 := new(CreateShortURLShortURLCommand)
	sub = &cobra.Command{
		Use:   `short_url ["/links"]`,
		Short: ``,
		Long: `

Payload example:

{
   "url": "Quas ipsam dolor."
}`,
		RunE: func(cmd *cobra.Command, args []string) error { return tmp1.Run(c, args) },
	}
	tmp1.RegisterFlags(sub, c)
	sub.PersistentFlags().BoolVar(&tmp1.PrettyPrint, "pp", false, "Pretty print response body")
	command.AddCommand(sub)
	app.AddCommand(command)
	command = &cobra.Command{
		Use:   "getShortURL",
		Short: `Get a short url by hash`,
	}
	tmp2 := new(GetShortURLShortURLCommand)
	sub = &cobra.Command{
		Use:   `short_url ["/links/decode/SHORT_URL_HASH"]`,
		Short: ``,
		RunE:  func(cmd *cobra.Command, args []string) error { return tmp2.Run(c, args) },
	}
	tmp2.RegisterFlags(sub, c)
	sub.PersistentFlags().BoolVar(&tmp2.PrettyPrint, "pp", false, "Pretty print response body")
	command.AddCommand(sub)
	app.AddCommand(command)
	command = &cobra.Command{
		Use:   "redirectShortURL",
		Short: `Redirect a short url`,
	}
	tmp3 := new(RedirectShortURLShortURLCommand)
	sub = &cobra.Command{
		Use:   `short_url ["/links/redirect/SHORT_URL_HASH"]`,
		Short: ``,
		RunE:  func(cmd *cobra.Command, args []string) error { return tmp3.Run(c, args) },
	}
	tmp3.RegisterFlags(sub, c)
	sub.PersistentFlags().BoolVar(&tmp3.PrettyPrint, "pp", false, "Pretty print response body")
	command.AddCommand(sub)
	app.AddCommand(command)
}

func intFlagVal(name string, parsed int) *int {
	if hasFlag(name) {
		return &parsed
	}
	return nil
}

